function vogalOuConsoante(x){
    
    if (x == "a"|| x == "A" || 
            x == "e"|| x == "E" || 
            x == "i"|| x == "I" ||
            x == "o"|| x == "O" || 
            x == "u"|| x == "U") {
        
        return "Vogal";
        
    } else {
        return "Consoante";
    }
}

