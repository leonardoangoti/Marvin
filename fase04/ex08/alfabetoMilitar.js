function alfabetoMilitar(x) {
       if (x == "a" || x == "A") {
           return "alfa";
       } else if (x == "e" || x == "E") {
           return "echo";
       } else if (x == "i" || x == "I") {
           return "india";
       } else if (x == "o" || x == "O") {
           return "oscar";
       } else if (x == "u" || x == "U") {
           return "uniform";
       } else {
           return false;
       }
}

